/**
 * Normalizes a string for robust name/handle lookup.
 */
export function normalizeKey(str: string): string {
  return str
    .toLowerCase()
    .replace(/[^a-z0-9]/gi, "")
    .trim();
}

/**
 * Resolves the real email for a handle or influencer name.
 * Returns undefined if no mapping exists.
 */
export function getRealEmailForInfluencer(handle: string, name?: string): string | undefined {
  const handleKey = normalizeKey(handle);
  if (INFLUENCER_EMAILS_MAP[handleKey]) {
    return INFLUENCER_EMAILS_MAP[handleKey];
  }

  if (name) {
    const nameKey = normalizeKey(name);
    if (INFLUENCER_EMAILS_MAP[nameKey]) {
      return INFLUENCER_EMAILS_MAP[nameKey];
    }
  }

  return undefined;
}
